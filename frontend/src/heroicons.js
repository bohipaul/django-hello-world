// Fonction helper pour créer un élément avec une icône
export function createIconElement(iconName, className = 'w-6 h-6', style = '') {
  const container = document.createElement('div');
  container.innerHTML = HeroIcons[iconName] || HeroIcons.users;
  const svg = container.firstElementChild;
  
  if (className) {
    svg.className = className;
  }
  
  if (style) {
    svg.setAttribute('style', style);
  }
  
  return svg;
}